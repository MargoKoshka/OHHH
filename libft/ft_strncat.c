#include <string.h>

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	t;

	i = 0;
	t = 0;
	while (*s1)
	{
		s1++;
		t++;
	}
	while (*s2 && i < n)
	{
		*s1 = *s2;
		s1++;
		s2++;
		i++;
		t++;
	}
	*s1 = '\0';
	return (s1 - t);
}
